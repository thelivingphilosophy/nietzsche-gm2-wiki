#!/usr/bin/env node
/**
 * Build step: compile all wiki content pages into a single JS module
 * that the Netlify chat function imports statically.
 *
 * Output: netlify/functions/wiki-pages.js
 *   export const index = "...index.md content...";
 *   export const pages = { "concepts/bad-conscience.md": "...", ... };
 */

import { readdirSync, readFileSync, writeFileSync, mkdirSync, statSync } from 'fs';
import { join, relative, extname } from 'path';
import { fileURLToPath } from 'url';
import { dirname } from 'path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..');
const contentDir = join(repoRoot, 'content');
const outputDir = join(repoRoot, 'netlify', 'functions');
const outputFile = join(outputDir, 'wiki-pages.js');

function walkMd(dir, root = dir) {
  const results = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const fullPath = join(dir, entry.name);
    if (entry.isDirectory()) {
      results.push(...walkMd(fullPath, root));
    } else if (extname(entry.name) === '.md') {
      const relPath = relative(root, fullPath).replace(/\\/g, '/');
      results.push({ path: relPath, content: readFileSync(fullPath, 'utf8') });
    }
  }
  return results;
}

const all = walkMd(contentDir);
const indexFile = all.find(f => f.path === 'index.md');
const rest = all.filter(f => f.path !== 'index.md');

if (!indexFile) {
  console.error('ERROR: content/index.md not found');
  process.exit(1);
}

const pagesObj = Object.fromEntries(rest.map(f => [f.path, f.content]));

mkdirSync(outputDir, { recursive: true });

writeFileSync(outputFile, [
  '// AUTO-GENERATED by scripts/compile-wiki-context.mjs — do not edit',
  `// Generated: ${new Date().toISOString()}`,
  `// Pages: ${rest.length} + index`,
  '',
  `export const index = ${JSON.stringify(indexFile.content)};`,
  '',
  `export const pages = ${JSON.stringify(pagesObj, null, 2)};`,
  '',
].join('\n'));

console.log(`✓ Compiled wiki: index.md + ${rest.length} pages → netlify/functions/wiki-pages.js`);
